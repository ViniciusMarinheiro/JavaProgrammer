package aula05;

public class OperadorUnario {
	
	public static void main(String[] args) {
		int num = 1;
		System.out.println("Valor do num = " + num);
		
		num = num + 1;
		System.out.println("Valor do num = " + num);
		
		num += 1;
		System.out.println("Valor do num = " + num);
		
		num++;
		System.out.println("Valor do num = " + num);
		
		++num;
		System.out.println("Valor do num = " + num);
		
		System.out.println("Valor do num = " + ++num);
		
		
	}

}
